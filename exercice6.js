// Fonction calculerSomme avec deux paramètres, la variable somme permet de faire l'addition, return permet de donner le résulat comme valeur à la fonction :
function calculerSomme(nombre1, nombre2) {
  var somme = nombre1 + nombre2;
  return somme;
}

// Appel de la fonction + exemples de nombres
var resultat = calculerSomme(5, 3);
console.log("La somme est : " + resultat);
